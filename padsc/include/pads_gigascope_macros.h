#ifndef PDCI_MacroArg2String
#define PDCI_MacroArg2String(s) #s
#endif

#define HDR_GET_FN(ty, field) \
int get_pads_ ## field (struct packet *p, ty *r) { \
  if (global_hdr_pd.field.nerr) return -1; \
  *r = (ty)global_hdr.field; \
  return 0; \
}

